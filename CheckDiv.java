package JavaProgram;

public class CheckDiv {
public static void main(String []Args)
{
int num=110;
if((num%5==0)&&(num%11==0))
{
	System.out.println("Number " +num+ "is divisible by both 5 and 11");
}else
	{
		System.out.println("Number " +num+ "is not divisible by both 5 and 11");
	}
}
}

